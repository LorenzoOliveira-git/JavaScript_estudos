// в”Ђв”Ђ TAREFA 02 в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
// Leia o data-preco do #prod-001 com getAttribute e exiba no console.
// dica: card1.getAttribute("data-preco")
const preco1 = card1.getAttribute("data-preco");
console.log("PreГ§o 1:", preco1); // в†’ "29.90"

// в”Ђв”Ђ TAREFA 03 в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
// Use dataset para ler data-categoria do #prod-001 e exibir no console.
// dica: card1.dataset.categoria
console.log("Categoria 1:", card1.dataset.categoria); // в†’ "aves"

// в”Ђв”Ђ TAREFA 04 в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
// Selecione o .prod-preco dentro do #prod-001 e atualize com textContent.
// O preГ§o deve exibir: "R$ 29,90"
// dica: card1.querySelector(".prod-preco")
const precoEl1 = card1.querySelector(".prod-preco");
precoEl1.textContent = "R$ 29,90";

// в”Ђв”Ђ TAREFA 05 в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
// Selecione o .prod-badge do #prod-001.
// Use setAttribute para adicionar data-status="disponivel" nele.
// dica: card1.querySelector(".prod-badge")  в†’  .setAttribute(...)
const badge1 = card1.querySelector(".prod-badge");
badge1.setAttribute("data-status", "disponivel");
badge1.textContent = "DisponГ­vel";

// в”Ђв”Ђ TAREFA 06 в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
// Selecione TODOS os .card-produto com querySelectorAll.
// Use forEach para: ler data-preco via getAttribute e exibir no console.
// dica: document.querySelectorAll(".card-produto")
const todosCards = document.querySelectorAll(".card-produto");
todosCards.forEach(card => {
  const nome  = card.querySelector(".prod-nome").textContent;
  const preco = card.getAttribute("data-preco");
  console.log(`${nome}: R$ ${preco}`);
});

// в”Ђв”Ђ TAREFA 07 в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
// Para CADA card, atualize o .prod-preco com o valor de data-preco.
// Formatar como "R$ X,XX" (substitua o ponto por vГ­rgula).
// dica: preco.replace(".", ",")
todosCards.forEach(card => {
  const precoRaw = card.getAttribute("data-preco");
  const precoFormatado = "R$ " + precoRaw.replace(".",",");
  card.querySelector(".prod-preco").textContent = precoFormatado;
});

// в”Ђв”Ђ TAREFA 08 в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
// Para CADA card, atualize o .prod-badge com o status de disponibilidade.
// Se data-disponivel="true" в†’ badge.textContent = "DisponГ­vel" + data-status="disponivel"
// Se data-disponivel="false" в†’ badge.textContent = "IndisponГ­vel" + data-status="indisponivel"
// dica: card.dataset.disponivel === "true"
todosCards.forEach(card => {
  const badge      = card.querySelector(".prod-badge");
  const disponivel = card.dataset.disponivel;
  if (disponivel === "true") {
    badge.textContent = "DisponГ­vel";
    badge.setAttribute("data-status", "disponivel");
  } else {
    badge.textContent = "IndisponГ­vel";
    badge.setAttribute("data-status", "indisponivel");
  }
});

// в”Ђв”Ђ TAREFA 09 в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
// Atualize o #total-produtos com o nГєmero total de cards.
// Atualize o #total-disponiveis com quantos tГЄm data-disponivel="true".
// dica: todosCards.length  |  filter com dataset.disponivel === "true"
const totalEl      = document.getElementById("total-produtos");
const disponiveisEl = document.getElementById("total-disponiveis");

const qtdTotal     = Array.from(todosCards).length;
const qtdDisponiveis = Array.from(todosCards).filter(c =>{
  if (c.dataset.disponivel === "true"){
    return c;
  }
}).length; // dica: use Array.from(todosCards).filter(...)

totalEl.querySelector("strong").textContent     = qtdTotal;
disponiveisEl.querySelector("strong").textContent = qtdDisponiveis;

// в”Ђв”Ђ VERIFICAГ‡ГѓO FINAL в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
